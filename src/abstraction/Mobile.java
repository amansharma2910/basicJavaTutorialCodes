package abstraction;
/*
* Abstraction can be used to make a basic framework of your Java code. In an abstraction class, you can just declare the various methods you are actually going to use in your code. However, you can only and only declare the methods. You can't add functionality to them. For that, you will need to inherit and override them, as given in the example package.
*/
public abstract class Mobile {
    public abstract void phoneCall();
    public abstract void camera();

}

